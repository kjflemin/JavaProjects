public class MyMath {

	public int Add(int a, int b) {
		return a+b;
		
	}
	
	public int Subtract(int a, int b) {
		return a-b;
	}
	
	public int Multiply(int a, int b) {
		return a*b;
	}
	
	public int Divide(int numerator, int divisor) {
		return Math.abs(numerator / divisor);
		
	}
}
